1. DDL
	- 데이터베이스의 객체를 생성,수정,삭제하는 언어
	- 종류 : CREATE, alster
	
2. CREATE TABLE 표현법 
	- CREATE TABLE 테이블명 (칼럼명1 자료형,	
					   칼럼명2 자료형, 				
					   칼럼명3 자료형)

3. 자주 사용되는 자료형의 종류
-varchar(byte ) : 가변길이 문자열 (최대65535 바이트)
- text : 대용량 텍스트 타입 (주로 게시판 내용등에 사용)
- int : 숫자 (정수형)
- decimal(p , s ) : 숫자 (실수형, 문자열 기반 소수점)
	> 문자열 기반 고정 소수점이기 때문에 오차가 존재하지 않음
	> p : 전체 자릿수
	> s : 소수점 기준 자릿수