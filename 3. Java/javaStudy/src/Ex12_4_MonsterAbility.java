public class Ex12_4_MonsterAbility {
	private int damage = 5; // 몬스터의 공격력
	private int hp = 100; // 몬스터의 체력
	
	public int getDamage() {
		return damage;
	}
	public void setDamage(int damage) {
		this.damage = damage;
	}
	public int getHp() {
		return hp;
	}
	public void setHp(int hp) {
		this.hp = hp;
	}
}
